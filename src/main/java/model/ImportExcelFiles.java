package model;

import java.util.List;

public class ImportExcelFiles {
	
	private List<String> dropdowns;
	
	private String selectedStatus;
	
	private int indexSelected;

	/**
	 * @return the dropdowns
	 */
	public List<String> getDropdowns() {
		return dropdowns;
	}

	/**
	 * @param dropdowns the dropdowns to set
	 */
	public void setDropdowns(List<String> dropdowns) {
		this.dropdowns = dropdowns;
	}

	/**
	 * @return the selectedStatus
	 */
	public String getSelectedStatus() {
		return selectedStatus;
	}

	/**
	 * @param selectedStatus the selectedStatus to set
	 */
	public void setSelectedStatus(String selectedStatus) {
		this.selectedStatus = selectedStatus;
	}

	/**
	 * @return the indexSelected
	 */
	public int getIndexSelected() {
		return indexSelected;
	}

	/**
	 * @param indexSelected the indexSelected to set
	 */
	public void setIndexSelected(int indexSelected) {
		this.indexSelected = indexSelected;
	}
	
	
	
	

}
