package model;


public class PaymentMethod {

	private int rec_No;
	private String name;
	
	
	public int getRec_No() {
		return rec_No;
	}
	public void setRec_No(int rec_No) {
		this.rec_No = rec_No;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	
	
}
