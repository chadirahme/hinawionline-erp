package model;

public class SchoolModel {

	
	private int schoolid;
	private String enschoolname;
	private String arschoolname;
	private String enaddress;
	private String araddress;
	private String website;
	private String email;
	private String phoneno;
	private String mobile;
	private String faxno;
	private String pobox;
	
	public String getEnaddress() {
		return enaddress;
	}
	public void setEnaddress(String enaddress) {
		this.enaddress = enaddress;
	}
	public String getAraddress() {
		return araddress;
	}
	public void setAraddress(String araddress) {
		this.araddress = araddress;
	}
	public String getWebsite() {
		return website;
	}
	public void setWebsite(String website) {
		this.website = website;
	}
	public String getEmail() {
		return email;
	}
	public void setEmail(String email) {
		this.email = email;
	}
	public String getPhoneno() {
		return phoneno;
	}
	public void setPhoneno(String phoneno) {
		this.phoneno = phoneno;
	}
	public String getMobile() {
		return mobile;
	}
	public void setMobile(String mobile) {
		this.mobile = mobile;
	}
	public String getFaxno() {
		return faxno;
	}
	public void setFaxno(String faxno) {
		this.faxno = faxno;
	}
	public String getPobox() {
		return pobox;
	}
	public void setPobox(String pobox) {
		this.pobox = pobox;
	}
	public int getSchoolid() {
		return schoolid;
	}
	public void setSchoolid(int schoolid) {
		this.schoolid = schoolid;
	}
	public String getEnschoolname() {
		return enschoolname;
	}
	public void setEnschoolname(String enschoolname) {
		this.enschoolname = enschoolname;
	}
	public String getArschoolname() {
		return arschoolname;
	}
	public void setArschoolname(String arschoolname) {
		this.arschoolname = arschoolname;
	}
	
}
